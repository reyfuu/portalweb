<p><b>Helloindo</b> - Kasus Covid-19 di Bangkalan terus menunjukkan peningkatan. <br>
<br>
Berdasarkan data yang dihimpun oleh Kompas.com, Minggu (6/6), kasus <br>
positif corona di Bangkalan bertambah 25. <br>
<br>
Lalu pada Senin (7/6), bertambah 40. Pada Selasa (8/6) kemarin, bertambah <br>
80 kasus. Sehingga, total dalam 3 hari, ada penambahan 145 kasus baru <br>
di Bangkalan.<br>
<br>
Untuk mengantisipasi lonjakan kasus Covid-19, Komite Olahraga Nasional <br>
Indonesia Bangkalan, memberikan proteksi kepada para atlet dan para pelaku <br>
olahraga di Bangkalan. <br>
<br>
Ketua umum KONI Bangkalan Moch Fauzan Ja'far mengaku sudah lama <br>
mengajukan vaksinasi ke Dinas Kesehatan Bangkalan agar semua pelaku <br>
olahraga terutama atlet mendapatkan suntikan vaksin. <br>
<br>
"Eksekusinya baru dilaksanakan kemarin di ruang rapat pendopo Bupati <br>
Bangkalan, agar mereka terlindungi dari wabah ini. Sekarang Bangkalan betul-<br>
betul pandemi," kata dia, Rabu (9/6/2021).<br>
<br>
Pelaku olahraga yang menjalani vaksinasi terdiri atas pengurus KONI,  <br>
pengurus cabang olahraga (cebor), pelatih official, dan atlet binaan yang <br>
diproyeksikan untuk pekan olahraga provinsi tahun 2022 mendatang. <br>
<br>
KONI mengajukan 200 pelaku olahraga untuk dilakukan vaksinasi. <br>
<br>
"Kemarin vaksin untuk pelaku olahraga di dalamnya ada atlet binaan yang <br>
harus divaksin, karena kebanyakan pelaku olahraga ada kalangan Guru dan <br>
ASN, jadi sudah ada yang melakukan vaksin," terang dia.<br>
<br>
Fauzan mengatakan, vaksinasi untuk insan olahraga dilakukan untuk menjaga <br>
imunitas dan kekebalan tubuh di masa pandemi Covid-19. Apalagi kasus <br>
Covid-19 di Bangkalan tengah menjadi perhatian banyak pihak. <br>
<br>
"Kami juga sebagai pelaku olahraga juga ingin memberikan contoh bahwa <br>
menjaga kesehatan dengan langkah vaksin itu sangat penting," cetus dia. <br>
<br>
Fauzan juga berterima kasih kepada Bupati Bangkalan atas dukungannya terhadap<br>
pelaku olahraga di Bangkalan, salah satunya terkait vaksinasi.<br>
<br>
Di sisi lain, prioritas penyelenggaraan pemusatan latihan kabupaten <br>
(puslatkab) pun sudah dilakukan. <br>
"Dukungan pemerintah kabupaten kepada kami sudah sangat maksimal," tutur <br>
dia.<br>
<br>
Fauzan menyampaikan bahwa selama ini kegiatan puslatkab bergulir dengan <br>
tetap menegakkan protokol kesehatan dan menghindari kerumunan. <br>
"Di setiap sesi latihan yang dilakukan oleh cabor tetap ditekankan agar tetap<br>
menjalani prokes," pungkas dia.<br>
</p>